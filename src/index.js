const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {

    let stack = [];

    for (let i = 0; i < expr.length; i+=10) {               
        let symbol = expr.slice(i, i+10);   
        
        if ( symbol === "**********" ) {
            stack.push(" ");
        } else {
            let code = symbol.replace(/11/g, "-").replace(/0/g, "").replace(/1/g, "\.");
            stack.push(MORSE_TABLE[code]);     
        }   
    }
       
    return stack.join('');
}

module.exports = {
    decode
}